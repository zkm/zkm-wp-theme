/**
 * Add icons to social menu item labels.
 *
 * @param string   $title Menu item title.
 * @param WP_Post  $item  Menu item object.
 * @param stdClass $args  Menu args.
 * @param int      $depth Menu depth.
 *
 * @return string
 */
function zkm_custom_add_social_menu_icons( $title, $item, $args, $depth ) {
    if ( empty( $args->theme_location ) || 'social' !== $args->theme_location ) {
        return $title;
    }

    $icon_svg = zkm_custom_get_social_icon_svg( $item );

    return '<span class="zkm-social-icon" aria-hidden="true">' . $icon_svg . '</span><span class="screen-reader-text">' . $title . '</span>';
}
add_filter( 'nav_menu_item_title', 'zkm_custom_add_social_menu_icons', 10, 4 );

