if ( ! function_exists( 'zkm_custom_setup' ) ) {
    /**
     * Theme setup.
     *
     * @return void
     */
    function zkm_custom_setup() {
        add_theme_support( 'title-tag' );
        add_theme_support( 'post-thumbnails' );
        add_theme_support( 'html5', array( 'search-form', 'gallery', 'caption', 'style', 'script' ) );
        add_theme_support( 'custom-logo' );

        register_nav_menus(
            array(
                'primary' => __( 'Primary Menu', 'zkm-wp-theme' ),
                'footer'  => __( 'Footer Menu', 'zkm-wp-theme' ),
                'social'  => __( 'Social Menu', 'zkm-wp-theme' ),
            )
        );
    }
}
add_action( 'after_setup_theme', 'zkm_custom_setup' );

/**
 * Enqueue theme styles.
 *
 * @return void
 */
function zkm_custom_enqueue_assets() {
    wp_enqueue_style(
        'zkm-wp-theme-fonts',
        'https://fonts.googleapis.com/css2?family=Inter:wght@400;700&display=swap',
        array(),
        null
    );

    wp_enqueue_style(
        'zkm-wp-theme-style',
        get_stylesheet_uri(),
        array( 'zkm-wp-theme-fonts' ),
        wp_get_theme()->get( 'Version' )
    );

    wp_enqueue_script(
        'zkm-wp-theme-interactions',
        get_template_directory_uri() . '/assets/js/theme.js',
        array(),
        wp_get_theme()->get( 'Version' ),
        true
    );
}
add_action( 'wp_enqueue_scripts', 'zkm_custom_enqueue_assets' );

/**
 * Add rel="me" to social menu links.
 *
 * @param array    $atts Menu item attributes.
 * @param WP_Post  $item Menu item data object.
 * @param stdClass $args Nav menu arguments.
 * @param int      $depth Depth.
 *
 * @return array
 */
function zkm_custom_social_rel_me( $atts, $item, $args, $depth ) {
    if ( isset( $args->theme_location ) && 'social' === $args->theme_location ) {
        $atts['rel'] = isset( $atts['rel'] ) ? trim( $atts['rel'] . ' me' ) : 'me';
    }

    return $atts;
}
add_filter( 'nav_menu_link_attributes', 'zkm_custom_social_rel_me', 10, 4 );

